/*
Math.random Method
Generates a random decimal value between 0 and 0.99
*/

var decimal = Math.random();

console.log(decimal);
console.log(decimal * 10);

/*
Math.ceil Method
Rounds a decilam to the nearest interger
*/

var decimalNum = decimal * 100
console.log(decimalNum);
console.log(Math.ceil(decimalNum));

